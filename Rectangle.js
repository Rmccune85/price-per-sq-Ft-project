// Rectangle Class Definition
export default class Rectangle {
    // Constructor to initialize Length and Width
    constructor(length, width) {
        this.length = length;
        this.width = width;
    }

    // Method to calculate area (Length x Width)
    GetArea() {
        return this.length * this.width;
    }
}

